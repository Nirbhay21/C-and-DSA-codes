#include <iostream>
using namespace std;

int main()
{
    cout << "4 >> 1 = " << (4 >> 1) << endl;
    cout << "4 >> 2 = " << (4 >> 2) << endl;
    cout << "16 >> 2 = " << (16 >> 2) << endl;
    cout << "16 >> 1 = " << (16 >> 1) << endl;
    cout << "100 >> 5 = " << (100 >> 5) << endl;
    return 0;
}
